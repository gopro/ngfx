#include "ngfx/graphics/PipelineCache.h"
using namespace ngfx;

Pipeline *PipelineCache::get(const std::string &key) {
  if (v.find(key) == v.end())
    return nullptr;
  return v[key].get();
}
void PipelineCache::add(const std::string &key, Pipeline *value) {
  v[key].reset(value);
}